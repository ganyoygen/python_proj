# Operasi Standar (+ - / *)

# Operasi Eksponen ** (pangkat)
a = 10
b = 3
hasil = a ** b
print(a,"**",b,"=",hasil)

# Operasi Modulus % (hasil sisa bagi)
a = 10
b = 3
hasil = a % b
print(a,"%",b,"=",hasil)

# Operasi Floor division // (hasil bagi dibulatkan kebawah)
a = 10
b = 3
hasil = a // b
print(a,"//",b,"=",hasil)

# Prioritas operasi, Operational Precedence
