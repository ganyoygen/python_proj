#Program Sederhana

print("\n===Konversi Suhu===\n")
fahrenheit = float(input("Masukan suhu dalam F:"))
print('Fahrenheit =',fahrenheit)


#Celcius
celcius = 5 / 9 * (fahrenheit - 32)
print('Celcius =',celcius)

#Reamur
reamur = 4 / 9 * (fahrenheit - 32)
print('Reamur =',reamur)

#Kelvin
kelvin = celcius + 273.15
print('Kelvin =',kelvin)







